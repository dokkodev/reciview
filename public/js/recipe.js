var recipe_card_template = function(recipe, key){
	return [
		'<div class="col col-3">',
			'<div class="card recipe-card text-center" data-id="',key,'">',
				'<div class="card-header">',
					'<h5 class="card-title">',recipe.recipe_id,'</h5>','</div>',
				'<div class="card-body">',
					'<img class="card-img" src="',recipe.src,'">',
					'<div class="row edit-enter-wrapper">',
						'<button type="button" class="btn btn-secondary edit-button col">수정</button>',
						'<button type="button" class="btn btn-primary enter-button col">리뷰 보기',
							(recipe.new_review ? '<div class="notification">' + recipe.new_review + '</div>' : ''),
						'</button>',
					'</div>',
				'</div>',
			'</div>',
		'</div>'
	].join('');
};

$(document).on('click', '.enter-button', function() {
    location.href = '/review?id=' + $(this).parents('.recipe-card').data('id');
});

$(document).on('click', '.edit-button', function() {
    location.href = '/recipe_add?id=' + $(this).parents('.recipe-card').data('id');
});

$(document).ready(function(){
    database.ref('Recipes').once('value').then(function(snapshot) {
        if (snapshot.val()) {
            recipe_list = Object.values(snapshot.val());
        }
        recipe_list.map(function(recipe, idx) {
            $('#newadd-card').before(recipe_card_template(recipe, 'dummyRecipe'+idx));
        });
    });	
});
